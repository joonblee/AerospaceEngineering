  weigp(6,1) =  0.555555555555556; weigp(3,2) =  0.888888888888889;
  weigp(7,1) =  0.555555555555556; weigp(1,2) =  0.555555555555556;
  weigp(8,1) =  0.888888888888889; weigp(2,2) =  0.555555555555556;
  weigp(9,1) =  0.555555555555556; weigp(3,2) =  0.555555555555556;
elseif ngaus==4
  posgp(1,1)  = -0.861136311590453; posgp(1,2)  = -0.861136311590453;
  posgp(2,1)  = -0.339981043583856; posgp(2,2)  = -0.861136311590453;
  posgp(3,1)  =  0.339981043583856; posgp(3,2)  = -0.861136311590453;
