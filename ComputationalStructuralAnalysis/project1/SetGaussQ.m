  posgp(6,1)  = -0.339981043583856; posgp(6,2)  = -0.339981043583856;
  posgp(7,1)  =  0.339981043583856; posgp(7,2)  = -0.339981043583856;
  posgp(8,1)  =  0.861136311590453; posgp(8,2)  = -0.339981043583856;
  posgp(9,1)  = -0.861136311590453; posgp(9,2)  =  0.339981043583856;
  posgp(10,1) = -0.339981043583856; posgp(10,2) =  0.339981043583856;
  posgp(11,1) =  0.339981043583856; posgp(11,2) =  0.339981043583856;
