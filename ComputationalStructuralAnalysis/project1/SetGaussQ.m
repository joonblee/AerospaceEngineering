  weigp(9,1)  = 0.347854845137454; weigp(9,2)  = 0.652145154862526;
  weigp(10,1) = 0.652145154862526; weigp(10,2) = 0.652145154862526;
  weigp(11,1) = 0.652145154862526; weigp(11,2) = 0.652145154862526;
  weigp(12,1) = 0.347854845137454; weigp(12,2) = 0.652145154862526;
